package com.student.bookdiary.model;

/**
 * Перелік можливих статусів читання книги в читацькому щоденнику.
 * Визначає, чи книга вже прочитана, чи знаходиться у списку бажань.
 */
public enum ReadingStatus {
    /**
     * Статус, що вказує на те, що книга була прочитана користувачем.
     */
    READ("Прочитана"),

    /**
     * Статус, що вказує на те, що користувач має намір прочитати цю книгу в майбутньому.
     * Книга з таким статусом зазвичай знаходиться у "списку бажань".
     */
    WANT_TO_READ("Хочу прочитати");

    /**
     * Назва статусу, призначена для відображення в користувацькому інтерфейсі.
     * Наприклад, у випадаючих списках, мітках або таблицях.
     */
    private final String displayName;

    /**
     * Конструктор для елемента переліку {@code ReadingStatus}.
     * Ініціалізує статус його назвою для відображення.
     *
     * @param displayName Назва статусу, яка буде використовуватися в UI.
     */
    ReadingStatus(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Повертає назву статусу, зручну для відображення користувачеві.
     * Цей метод надає локалізоване або більш описове представлення статусу.
     *
     * @return Назва статусу, призначена для UI.
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * Повертає рядкове представлення елемента переліку.
     * За замовчуванням, цей метод повертає назву статусу, призначену для відображення (displayName).
     * Це зручно для використання в компонентах JavaFX, таких як ComboBox,
     * які автоматично викликають `toString()` для відображення елементів списку.
     *
     * @return Назва статусу для UI (еквівалентно {@link #getDisplayName()}).
     */
    @Override
    public String toString() {
        return displayName;
    }
}