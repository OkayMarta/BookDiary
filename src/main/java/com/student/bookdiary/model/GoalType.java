package com.student.bookdiary.model;

/**
 * Перелік можливих типів цілей читання.
 * Визначає, чи є ціль щомісячною, щорічною або загальною (без прив'язки до періоду).
 */
public enum GoalType {
    /**
     * Ціль, встановлена на певний місяць.
     */
    MONTHLY("За місяць"),

    /**
     * Ціль, встановлена на певний рік.
     */
    YEARLY("За рік"),

    /**
     * Загальна ціль, не прив'язана до конкретного місяця або року.
     * Наприклад, "Прочитати 100 книг загалом".
     */
    TOTAL("Загалом");

    /**
     * Назва типу цілі, призначена для відображення в користувацькому інтерфейсі.
     * Наприклад, у випадаючих списках або мітках.
     */
    private final String displayName;

    /**
     * Конструктор для елемента переліку {@code GoalType}.
     * Ініціалізує тип цілі його назвою для відображення.
     *
     * @param displayName Назва типу цілі, яка буде використовуватися в UI.
     */
    GoalType(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Повертає назву типу цілі, зручну для відображення користувачеві.
     * Цей метод надає локалізоване або більш описове представлення типу цілі.
     *
     * @return Назва типу цілі, призначена для UI.
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * Повертає рядкове представлення елемента переліку.
     * За замовчуванням, цей метод повертає назву типу цілі, призначену для відображення (displayName).
     * Це зручно для використання в компонентах JavaFX, таких як ComboBox,
     * які автоматично викликають `toString()` для відображення елементів списку.
     *
     * @return Назва типу цілі для UI (еквівалентно {@link #getDisplayName()}).
     */
    @Override
    public String toString() {
        return displayName;
    }
}